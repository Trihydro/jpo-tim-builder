package com.trihydro.timCreator.model;

public class ComputedLane
{
	 private Integer laneID;
     private Integer offsetSmallX;
     private Integer offsetLargeX;
     private Integer offsetSmallY;
     private Integer offsetLargeY;
     private Integer angle;
     private Integer xScale;
     private Integer yScale;
     public Integer getLaneID() {
        return laneID;
     }
     public void setLaneID(Integer laneID) {
        this.laneID = laneID;
     }
     public Integer getOffsetSmallX() {
        return offsetSmallX;
     }
     public void setOffsetSmallX(Integer offsetSmallX) {
        this.offsetSmallX = offsetSmallX;
     }
     public Integer getOffsetLargeX() {
        return offsetLargeX;
     }
     public void setOffsetLargeX(Integer offsetLargeX) {
        this.offsetLargeX = offsetLargeX;
     }
     public Integer getOffsetSmallY() {
        return offsetSmallY;
     }
     public void setOffsetSmallY(Integer offsetSmallY) {
        this.offsetSmallY = offsetSmallY;
     }
     public Integer getOffsetLargeY() {
        return offsetLargeY;
     }
     public void setOffsetLargeY(Integer offsetLargeY) {
        this.offsetLargeY = offsetLargeY;
     }
     public Integer getAngle() {
        return angle;
     }
     public void setAngle(Integer angle) {
        this.angle = angle;
     }
     public Integer getxScale() {
        return xScale;
     }
     public void setxScale(Integer xScale) {
        this.xScale = xScale;
     }
     public Integer getyScale() {
        return yScale;
     }
     public void setyScale(Integer yScale) {
        this.yScale = yScale;
     }
}